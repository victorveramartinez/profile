// ===== APPLY LANGUAGE =====
function applyLang(lang) {
  const dict = i18n[lang];
  if (!dict) return;

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update WhatsApp button URL with localized message
  const waBtn = document.getElementById('waBtn');
  if (waBtn && dict['contact.wa.msg']) {
    const msg = encodeURIComponent(dict['contact.wa.msg']);
    waBtn.href = `https://wa.me/595983442170?text=${msg}`;
  }

  // Update html lang attribute
  document.documentElement.lang = langMeta[lang].htmlLang;

  // Update lang switcher display
  document.getElementById('langFlag').textContent = langMeta[lang].flag;
  document.getElementById('langCode').textContent = langMeta[lang].code;

  // Mark active option
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('vv-lang', lang);
}

