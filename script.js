const display = document.getElementById('codeDisplay');
let lineIdx = 0, charIdx = 0, rendered = [];

function findHtmlIndex(html, visibleCount) {
  let visible = 0, i = 0;
  while (i < html.length && visible < visibleCount) {
    if (html[i] === '<') { while (i < html.length && html[i] !== '>') i++; i++; }
    else { visible++; i++; }
  }
  return i;
}

function typeNext() {
  if (lineIdx >= codeLines.length) {
    setTimeout(() => { lineIdx = 0; charIdx = 0; rendered = []; display.innerHTML = ''; }, 3000);
    return;
  }
  const line = codeLines[lineIdx];
  const plain = line.replace(/<[^>]+>/g, '');
  if (charIdx <= plain.length) {
    rendered[lineIdx] = line.substring(0, findHtmlIndex(line, charIdx));
    display.innerHTML = rendered.join('\n');
    charIdx++;
    setTimeout(typeNext, 30);
  } else {
    rendered[lineIdx] = line;
    display.innerHTML = rendered.join('\n');
    lineIdx++; charIdx = 0;
    setTimeout(typeNext, line === '' ? 60 : 80);
  }
}
setTimeout(typeNext, 600);

